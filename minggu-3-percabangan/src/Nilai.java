public class Nilai {
    public static void main(String[] args) throws Exception {
        int nilai=80;

        if (nilai >= 80) {
            System.out.println("A");
        }else if(nilai >= 70){
            System.out.println("B");
        }else if(nilai >= 60){
            System.out.println("C");
        }else if(nilai >= 50){
            System.out.println("D");
        }else{
            System.out.println("E");
        }
    }
}
